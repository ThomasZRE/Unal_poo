# Ingresar por el usuario tres números enteros diferentes
entero1 = int(input("Ingrese el primer número entero: "))
entero2 = int(input("Ingrese el segundo número entero: "))
entero3 = int(input("Ingrese el tercer número entero: "))
# Encontrar el mayor de los tres números usando la función max()
mayor = max(entero1, entero2, entero3)
# Se muestra el resultado
print(f"El número mayor de los tres es: {mayor}")
